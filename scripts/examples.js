// This script copies JS glue code into the ./examples folder.
const fs = require('fs')
const path = require('path')

const exampleNames = [
  'spinning-shapes',
  'lettero',
  'capitals'
]

function readFile (filePath) {
  return new Promise((resolve, reject) => {
    fs.readFile(path.join(__dirname, '..', filePath), 'utf-8', (err, file) => {
      if (err) {
        reject(err)
      } else {
        resolve(file)
      }
    })
  })
}

function writeFile (filePath, fileContents) {
  return new Promise((resolve, reject) => {
    fs.writeFile(path.join(__dirname, '..', filePath), fileContents, err => {
      if (err) {
        reject(err)
      } else {
        resolve()
      }
    })
  })
}

Promise.all([
  readFile('src/js/db-local-storage.js'),
  readFile('src/js/talk-to-ports.js')
]).then(files => {
  const dbFile = files[0]
  const talkFile = files[1]
  const appFile = `/*
 * Glue code for elm-gameroom test game
 * Autogenerated by ./scripts/examples.js. Do not modify!
 */

${dbFile.replace('module.exports', 'window.db')}
${talkFile.replace('module.exports', 'window.talkToPorts')}
`
  return Promise.all(exampleNames.map(exampleName => (
    writeFile(`examples/${exampleName}/app.js`, appFile)
  )))
}).catch(err => {
  throw err
})
